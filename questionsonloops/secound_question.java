package questionsonloops;
//2. Java program to print all even numbers between 1 to 100 using for loop.

public class secound_question {
    
public static void main (String[] args)
    {
        for(int i=0; i<=100; i++ )
        {
            if(i%2==0)
            {
                System.out.println("Java program to print all even numbers between 1 to 100 using for loop.");
                System.out.println(i);
            }
        }
    }
}
