package questionsonloops;
// Java program to print all natural numbers from 1 to n using for loop.
public class first_question {

    public static void main (String[] args)
    {
        int n = 100;
        for(int i=1; i<=n; i++) {
            System.out.println("Java program to print all natural numbers from 1 to n using for loop.");
            System.out.println(i);
        }
    }
    
}